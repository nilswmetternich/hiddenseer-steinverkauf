let currentLang = 'de';

function applyLang(lang) {
  currentLang = lang;
  const t = TRANSLATIONS[lang] || TRANSLATIONS['de'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  document.documentElement.lang = lang;
}

function setLang(lang) {
  localStorage.setItem('hs_lang', lang);
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
  const btn = document.querySelector(`.lang-btn[onclick="setLang('${lang}')"]`);
  if (btn) btn.classList.add('active');
  applyLang(lang);
  // Re-render product grid if present
  if (typeof renderProducts === 'function') renderProducts();
}

function t(key) {
  const translations = TRANSLATIONS[currentLang] || TRANSLATIONS['de'];
  return translations[key] || key;
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
  const stored = localStorage.getItem('hs_lang') || 'de';
  const btn = document.querySelector(`.lang-btn[onclick="setLang('${stored}')"]`);
  if (btn) {
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
  }
  applyLang(stored);
});
